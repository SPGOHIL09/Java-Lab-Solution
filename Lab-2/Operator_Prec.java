public class Operator_Prec {
    public static void main(String[] args) {
        int a = 10 + 20 * 30;
        int b = 100 / 10 * 100;
        int c = 5 * 4 / 4 % 3;
        int d = 100 + 200 / 10 - 3 * 10;
        System.out.println("a: " + a);
        System.out.println("b: " + b);
        System.out.println("c: " + c);
        System.out.println("d: " + d);
    }
    
}
